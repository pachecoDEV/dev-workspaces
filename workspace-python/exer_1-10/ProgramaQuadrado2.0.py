print("Vamos calcular o quadrado do resultado da soma de alguns números.")
num1 = int(input("Digite o primeiro número:"))
num2 = int(input("Digite o segundo número:"))
num3 = int(input("Digite o terceiro número:"))
num4 = int(input("Digite o quarto número:"))
soma = num1 + num2 + num3 + num4
quadrado = soma * soma
print(f"O resultado da soma dos quatro números é: {soma}")
print(f"O quadrado do resultado obtido da soma é: {quadrado}")